// storing the macros for area and perimeter of triangle, square, circle
// linked with problem85.c

#define PI 3.141592653589793
#define ATRIA(h, b) (0.5 * h * b)
#define PERTRIA(x, y, z) (x + y + z)
#define ASQUA(x) (x * x)
#define PERSQUA(x) (x * 4)
#define ACIRC(r) (PI * r * r)
#define PERCIRC(r) (2 * PI * r)